				<a name="2-11"></a>
				<h3>I want to place multiple orders at the same store. Do I have to sign out and sign back in?</h3>
				<p class="a">Yes and no. Each time you click through to a store from
 Extrabux you start a tracking code. That tracking code is ended when 
you place an order, exit the store without ordering (including closing 
your browser), or time just runs out on the tracking. If you checkout 
once but want to keep shopping, even for the same item right after the 
first order, the store doesn't have a unique tracking code to assign to 
it because the tracking closed after the first checkout.</p>
				<p>If you want to place multiple orders (checking-out more than 
once), after you checkout the first time you need to go back to Extrabux
 and click back to the store website again through a link on Extrabux. 
That will start a new tracking code so you can get cash back the next 
time you checkout. The store may have saved items in your cart for you. 
You need to make sure your cart is empty each time you click over from 
Extrabux to make sure you get cash back. Then put the items back in your
 cart and checkout again for the next order.</p>
