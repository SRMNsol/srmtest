				<a name="2-10"></a>
				<h3>How does Extrabux and the store track my order so I get cash back?</h3>
				<p class="a">Anytime you click to a store site from Extrabux, we 
keep a record of what retailer you clicked to.  We DON'T know what you 
browse for at the merchant site.  Our tracking stops with the click to 
the merchant site. The store tracks when you enter their site and where 
you entered from (Extrabux). They may track the items you browse and the
 items you place in your cart and what you order.  Within a few days of 
completing the order (after your order is shipped), the store should 
report the order back to Extrabux and we post the cash back to your 
account!</p>
				<p>There are a few things that can mess up the tracking and cancel the cash back:</p>
				<ul class="faq-list">
					<li>Only go to the store website from Extrabux. Going from ANYwhere
 else can mess up the tracking and Extrabux won't get credit for your 
order. Only use coupon codes that are posted on Extrabux. Most stores 
track the source of the coupon codes and if it didn't come from Extrabux
 then we won't get credit for your order.</li>

					<li>Only place items in your cart AFTER you click through from 
Extrabux. If you visit the store website and put items in your cart 
before you click through from Extrabux the store will not give Extrabux 
credit for referring your order.</li>
					<li>Don't wait too long before completing your order.  The idea is 
to be relatively quick and efficient so the tracking is clear and tracks
 back to Extrabux. If you get distracted (by work, kids, life, etc.) 
it's best to remove items from your cart, then go back to Extrabux and 
click through to the store website again to start a new tracking 
session.</li>
					<li>The tracking ends when you complete an order.  If you want to 
place another order, even for the same item, make sure you exit the 
store and click back through from Extrabux to start a new tracking 
session.  This way when you checkout again we can give you cash back on 
both orders.</li>
				</ul>
				<p>For a complete list of DO's and DON'Ts <a href="http://www.extrabux.com/help/do">click here</a></p>
